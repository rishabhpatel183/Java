public class Typeconversionandcasting {
  public static void main(String args[]) {
    
    byte b = 127;
    int i = b;

     System.out.println(i);
    
    int a = 257;
    byte t = (byte) a;

    System.out.println(t);
     
    int c = 192;
    float f = c;

    System.out.println(f);

    float l = 21.90f;
    int h = (int) l;

    System.out.println(h);
  }
}