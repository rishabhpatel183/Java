public class Forloop{
    public static void main (String [] args){
    
    for (int a = 1;a <= 10; a++) {
            System.out.println("Hi " + a);
        }  
    System.out.println("");
    
    for (int b = 10;b > 0;b--) {
            System.out.println("Hello " + b);
        }  
    }
}
